x = 20
y = 40
print("This is the value of x {} and the value of y is {}".format(x, y))

print("This is the value of x {1} and the value of y is {0}".format("Milk", "Tea"))

# Using keyword arguments in the format() method
print("This is the value of x {x} and the value of y is {y}".format(x="Milk", y="Tea"))
