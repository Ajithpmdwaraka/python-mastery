a = 10
b = 3

print(a + b)  # Addition → 13
print(a - b)  # Subtraction → 7
print(a * b)  # Multiplication → 30
print(a / b)  # Division → 3.333... will get an floating point number
print(a // b)  # Floor Division → 3 will get an integer number, avoids decimal part
print(a % b)  # Modulus → 1 (remainder)
print(a**b)  # Exponentiation → 1000 (10^3)
